#Equals

a=5

#plus equals(+=)
a+=2 #short for a=a+2 which will result in 7
print(a)

#minus equals(-=)
a-=3#short for a=a-3
print(a)


#star equals(*=)
a*=2 #short for a=a*2   
print(a)

#slash equals(*=)
a/=2 #short for a=a/2   
print(a)

#double star equals(*=)
a**=2 #short for a=a**2   
print(a)

#percent equals(*=)
a%=2 #short for a=a%2   
print(a)

#double slash equals(*=)
a//=2 #short for a=a//2   
print(a)



#some builtin function

#divmod()  finds quotient and remainder

a, b= divmod(14,5)
print(a,b)

#pow(a,b) raises a to b power
print(pow(5,2))

#sum()
print(sum([1,2,4,5,6,7]))


#max
print(max([1,2,4,5,6,7]))


#min
print(min([1,2,4,5,6,7,-8]))

#round(a,b) rounds a to decimal points b
a=56.782
print(round(a,1))

